package com.osb.william.domain;

public class Prm {
    public Prm(Integer prmId, Integer prmCode) {
        this.prmId = prmId;
        this.prmCode = prmCode;
    }
}
    public Integer getPrmId() {
        return prmId;
    }

    public void setPrmId(Integer prmId) {
        this.prmId = prmId;
    }

    public Integer getPrmCode() {
        return prmCode;
    }

    public void setPrmCode(Integer prmCode) {
        this.prmCode = prmCode;
    }

